using System;

namespace Cars
{
    class Program
    {
        static void Main(string[] args)
        {
            // Printing out cars in a loop
            string[] cars = { "Volvo", "BMW", "Ford", "Mazda" };
            for (int i = 0; i < cars.Length; i++)
            {
                Console.WriteLine(cars[i]);
            }
        }
    }
}







